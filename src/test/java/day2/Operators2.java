package day2;

public class Operators2 {

    public static void main(String[] args) {
        int i=100;
        i++;
        System.out.println(i); //what is difference between i-- and --i

        int j=100;
        ++j;
        System.out.println(j);

        int k=100; //what is difference between k-- and --k
        k--;
        System.out.println(k);

        int m=100;
        --m;
        System.out.println(m);

    }
}
